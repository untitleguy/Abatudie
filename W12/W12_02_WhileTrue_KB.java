package W12;

import java.util.Scanner;

public class W12_02_WhileTrue_KB {
    public static void main(String[] args) {
        Scanner kb = new Scanner(System.in);
        boolean YesorNo = true;

    while (YesorNo) { 
        System.out.print("Enter number [0] to escape :");
        int input = kb.nextInt();
        if (input == 0){
            YesorNo = false;
        }


    }
        
        // รับค่าจากคีย์บอร์ดเก็บไว้ในตัวแปร number
                
        // ตรวจสอบเงื่อนไข ถ้า number เท่ากับ 0 ให้ออกจากลูป
            // ออกจากลูป

        
    }
}
